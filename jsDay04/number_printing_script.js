
// Task 1: Write a program to print numbers from 1 to 10 using a for loop.
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Task 4: Write a program to calculate the sum of numbers from 1 to 10 using a while loop.
let sum = 0;
let j = 1;
while (j <= 10) {
    sum += j;
    j++;
}
console.log("Sum:", sum);
